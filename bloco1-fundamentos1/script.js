/*
Tipos de dados
1. Number (ponto flutuante e inteiro)
2. String (sequencia de caracteres)
3. Boolean (verdadeiro ou falso)
4. Undefined (valor vazio, utilizado em variáveis declaradas mas não definidas)
5. Null (similar à undefined)
6. Symbol (ES2015 valor único e imutável usado como identificador par aevitar colisões de nomes em propriedades de objetos)
7. BigInt (ES2020 números maiores que tipo Number não sustenta)

OBS: JavaScript é uma linguagem de tipagem dinânimca (tipo da variável é atribuído automaticamente)
*/

